namespace Unosquare.Tubular
{
    /// <summary>
    /// Supported data types.
    /// </summary>
    public enum DataType
    {
        /// <summary>
        /// String type
        /// </summary>
        String,

        /// <summary>
        /// Numeric type (int or float)
        /// </summary>
        Numeric,

        /// <summary>
        /// DataTime type
        /// </summary>
        DateTime,

        /// <summary>
        /// Only Date type
        /// </summary>
        Date,

        /// <summary>
        /// Boolean type
        /// </summary>
        Boolean,

        /// <summary>
        /// DataTime UTC type
        /// </summary>
        DateTimeUtc,
    }
}